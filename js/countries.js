/**
 * Look up coordinates for a country name (case-insensitive, with common alias support).
 * @param {string} name
 * @returns {{ lat: number, lng: number } | null}
 */
export function getCountryCoords(name) {
    if (!name) return null;
    const trimmed = name.trim();
    // Direct lookup
    if (COUNTRY_COORDS[trimmed]) return COUNTRY_COORDS[trimmed];
    // Case-insensitive
    const lower = trimmed.toLowerCase();
    for (const [key, val] of Object.entries(COUNTRY_COORDS)) {
        if (key.toLowerCase() === lower) return val;
    }
    return null;
}
